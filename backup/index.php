                            <h1>Bienvenidos a COIPRI 2014</h1>
                            <p>El Crecimiento inmobiliario de los últimos años,  la escasez &nbsp;de terrenos y el aumento importante de precios, ha generado  que los inversionistas se adecuen a la nueva demanda no sólo como crecimiento  vegetativo sino como solución a nuevas exigencias municipales que obligan a  buscar nuevas localizaciones y alternativas de productos &nbsp;para satisfacer  esta demanda.  Es vital que los  profesionales Inmobiliarios conozcamos esta nueva realidad para vislumbrar  alternativas y propuestas buscando mantener nuestra actividad en permanente  dinamismo, con &nbsp;crecimiento sostenido e innovador.</p>
                            <p><br>
                          Por ello, la Asociación  Peruana de Agentes Inmobiliarios (ASPAI) organiza el III Congreso Internacional  de Profesionales Inmobiliarios (COIPRI 2014), <strong><em>Perú: Nuevas Realidades y</em></strong> <strong><em>Oportunidades</em></strong>,  congregando a reconocidos especialistas nacionales e internacionales que  expondrán sus puntos de vista y compartirán sus experiencias con todos los  profesionales inmobiliarios interesados y comprometidos en hacer del sector un  modelo de organización y planificación para el país.  Este año se pondrá énfasis en los cuatro  sectores del Mercado Inmobiliario: vivienda, retail, oficinas e industria analizando  el crecimiento tanto en Lima como en provincias.<strong> </strong></p>
